<h2>Aide</h2>

<h3>Installation</h3>
<p><strong>Avant de procéder à l'activation du plugin Zoombox vous devez avoir installé et activé le plugin <a href="http://pluxml.org/static7/download#plugins">jquery</a> sans quoi Zoombox ne fonctionnera pas.</strong></p><br />

<h3>Utilisation</h3>

<p>Pour que le script se déclenche, vous devez ajouter manuellement la classe "zoombox" à vos liens:<br />
<pre style="font-size:1.3em;">&lt;a href="" class="zoombox"&gt;&lt;img src="" alt=""&gt;&lt;/a&gt;</pre>
<pre style="font-size:1.3em;">&lt;a href="" class="zoombox"&gt;Texte&lt;/a&gt;</pre><br />
Pour utiliser le mode gallerie de Zoombox, ce dernier doit être activé depuis la page de configuration du plugin. Ensuite, il vous suffit d'ajouter la classe "zoombox zgallery123" au lien pointant vers vos images:<br />
<pre style="font-size:1.3em;">&lt;a href="" class="zoombox zgallery1"&gt;&lt;img src="" alt=""&gt;&lt;/a&gt;</pre><br />
Pour utiliser Zoombox avec des vidéos (Youtube, Dailymotion, Vimeo) vous devez ajouter la classe "zoombox" au lien pointant vers la vidéo.
<pre style="font-size:1.3em;">&lt;a href="http://www.youtube.com/watch?v=8oejjWGFs6o" class="zoombox"&gtYoutube&lt;/a&gt;</pre>
